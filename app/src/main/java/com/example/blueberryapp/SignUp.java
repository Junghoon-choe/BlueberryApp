package com.example.blueberryapp;

public class SignUp {

    private String ET_ID;
    private String ET_Password;
    private String ET_Email;
    private String ET_Name;
    private String ET_PhoneNum;
    private String ET_Address;

    public String getET_ID() {
        return ET_ID;
    }

    public void setET_ID(String ET_ID) {
        this.ET_ID = ET_ID;
    }

    public String getET_Password() {
        return ET_Password;
    }

    public void setET_Password(String ET_Password) {
        this.ET_Password = ET_Password;
    }

    public String getET_Email() {
        return ET_Email;
    }

    public void setET_Email(String ET_Email) {
        this.ET_Email = ET_Email;
    }

    public String getET_Name() {
        return ET_Name;
    }

    public void setET_Name(String ET_Name) {
        this.ET_Name = ET_Name;
    }

    public String getET_PhoneNum() {
        return ET_PhoneNum;
    }

    public void setET_PhoneNum(String ET_PhoneNum) {
        this.ET_PhoneNum = ET_PhoneNum;
    }

    public String getET_Address() {
        return ET_Address;
    }

    public void setET_Address(String ET_Address) {
        this.ET_Address = ET_Address;
    }
}
